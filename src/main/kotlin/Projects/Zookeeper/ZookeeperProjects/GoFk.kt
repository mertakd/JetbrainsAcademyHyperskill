    val prompt = "Please enter the number of the habitat you would like to view: "

    // loop to check if userInput is correct & repeating as long as input is true
    do {
        val userInput = readLine()!! // getting userInput

        // checking input
        if (userInput != INPUT5) {
            println(animals5[userInput.toInt()]) // converting input to a number
